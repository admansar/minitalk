#include "minitalk.h"

void	to_binary(char a, int pid)
{
	int		i;
	char	b;

	i = 7;
	while (i != -1)
	{
		b = '0' + (a >> i & 1);
		if (b == '0')
		{
			kill(pid, SIGUSR1);
		}
		if (b == '1')
		{
			kill(pid, SIGUSR2);
		}
		usleep(100);
		i--;
	}
}
